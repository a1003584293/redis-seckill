// 错误码前两位为场景编号，后三位为错误编号
// 20000 表示正确返回，切勿定义
const errors = {
  //登录注册相关
  10001: '等登录！',
  10002: '用户不存在！',


  // 秒杀相关
  30001: '邮件验证码发送失败，请重新发送！',
  30002: '该产品并未有抢购活动！',
  30003: '该活动已结束！',
  30004: '该抢购活动还未开始！',
  30005: '该抢购活动已经结束！',
  30006: '该产品已经卖完了！',
  30007: '该用户已抢到该产品，无需再抢！',
  30008: '抢购失败!',
}

exports.throwException = (ctx) => {
  return (code, message) => {
    ctx.body = {
      code,
      message: message || errors[code],
    };
  };
}